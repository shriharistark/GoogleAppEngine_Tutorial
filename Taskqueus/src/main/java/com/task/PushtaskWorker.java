/**
 * Servlet implementation class PushtaskWorker
 */
@WebServlet("/pushtaskworker")
public class PushtaskWorker extends HttpServlet {
	private static final long serialVersionUID = 1L;
	DatastoreService datastore;
	
	public void init(ServletConfig config) throws ServletException {
		datastore = DatastoreServiceFactory.getDatastoreService();
	}

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		
		Key key = KeyFactory.stringToKey(request.getParameter("keyt"));
		String number = request.getParameter("numbert");
		
		Entity toUpdate;
		try {
			toUpdate = datastore.get(key);
			toUpdate.setProperty("Number", number);
			datastore.put(toUpdate);
			response.setStatus(200);
		} catch (EntityNotFoundException e) {
			response.getWriter().println("Entity key mishap");
			e.printStackTrace();
		}

	}

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// TODO Auto-generated method stub
		doGet(request, response);
	}

}
